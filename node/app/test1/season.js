var month = 7;
if (month <= 0)
    console.log("Unknown number");
else if (month < 3)
    console.log(month + " is winter");
else if (month < 6)
    console.log(month + " is spring");
else if (month < 9)
    console.log(month + " is summer");
else if (month < 12)
    console.log(month + " is fall");
